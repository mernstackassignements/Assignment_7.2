/// Javascript code to print multiple of 5 till 50

// for loop
console.log('Using for loop');
for(var idx = 1 ; idx <= 10 ; ++idx){
    console.log('5 x '+idx+' = ' + 5*idx);
}

// while loop
console.log('using while loop');
idx = 1;
while(idx <= 10){
    console.log('5 x '+idx+' = ' + 5*idx);
    ++idx;
}
